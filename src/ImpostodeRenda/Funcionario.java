package ImpostodeRenda;

public class Funcionario {

	public String nome;
	public double renda;
	public double gastos;

	public Funcionario(String nome, double renda, double gastos) {
		
		this.nome = nome;
		this.renda = renda;
		this.gastos = gastos;
	}
	
	public String getNome() {
		return nome;
	}
	
	public double getRenda() {
		return renda;
	}
	
	public double gastos() {
		return gastos;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public void setRenda(double renda) {
		this.renda = renda;
	}
	
	public void setGastos(double gastos) {
		this.gastos = gastos;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	



}
